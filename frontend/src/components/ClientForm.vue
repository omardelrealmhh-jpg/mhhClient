<script setup>
import { ref } from 'vue'
import axios from 'axios'

const form = ref({
  first_name: '',
  last_name: '',
  dob: '',
  ssn: '',
  phone: '',
  gender: '',
  sf_resident: '',
  neighborhood: '',
  demographic_info: '',
  language: '',
  language_other: '',
  highest_degree: '',
  employment_status: '',
  training_interest: '',
  referral_source: '',
  additional_notes: '',
})

const error = ref('')
const success = ref(false)
const isSubmitting = ref(false)

async function handleSubmit() {
  error.value = ''
  success.value = false
  isSubmitting.value = true

  try {
    const response = await axios.post('http://localhost:8000/api/clients/', form.value)
    if (response.status === 201 || response.status === 200) {
      success.value = true
      form.value = {
        first_name: '',
        last_name: '',
        dob: '',
        ssn: '',
        phone: '',
        gender: '',
        sf_resident: '',
        neighborhood: '',
        demographic_info: '',
        language: '',
        language_other: '',
        highest_degree: '',
        employment_status: '',
        training_interest: '',
        referral_source: '',
        additional_notes: '',
      }
    }
  } catch (err) {
    if (err.response?.status === 400) {
      error.value = 'Please check all required fields and try again.'
    } else if (err.response?.status === 409) {
      error.value = 'A client with this information already exists. Please contact us directly.'
    } else {
      error.value = 'Unable to submit application. Please try again or contact us at info@missionhiringhall.org'
    }
  } finally {
    isSubmitting.value = false
  }
}
</script>

